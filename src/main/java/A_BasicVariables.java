public class A_BasicVariables {
    public static void main(String[] args) {
        int a = 1;
        float b = 1.0f;
        double c = 1.0;
        char d = 'a';
        String e = "abcdefg";
        boolean f = true;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d);
        System.out.println("e = " + e);
        System.out.println("f = " + f);

    }
}
